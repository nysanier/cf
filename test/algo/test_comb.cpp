#include "comb.h"
#include "dump.h"


int main() {
    std::set<std::set<int>> all;
    fn::comball(5, 1, 1, all);
    assert(all.size() == 5);
    DUMP(5, 1, 1, all);

    fn::comball(5, 2, 3, all);
    assert(all.size() == 10+10);
    DUMP(5, 2, 3, all);

    fn::comball(5, 3, 5, all);
    assert(all.size() == 10+5+1);
    DUMP(5, 3, 5, all);

    // c(5,0) == c(5,5)
    fn::comball(5, 1, 5, all);
    assert(all.size() == 5+10+10+5+1);
    DUMP(5, 1, 5, all);

    return 0;
}


